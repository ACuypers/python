x = input('Digite algo ')
print('O tipo primitivo desse valor é ', type(x))
print('So tem espaço',x.isspace())
print('É um numero', x.isnumeric())
print('E alfabetico',x.isalpha())
print('E alfanumerico',x.isalnum())
print('Esta em maiuscula', x.isupper())
print('Esta em minuscula', x.islower())
print('Esta capitalizada',x.istitle()) # para ver se começa com maiusculaA





